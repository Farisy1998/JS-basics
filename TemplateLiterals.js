// In JS we have literals,
// object - {}
// string - "" , ''
// boolean - true, false
// template - ``

// while using String literal
let name = "John";
let message = "Hi " + name + ", This is my \n 'first' message.";
console.log(message);

// using template literal
let another = `Hi, ${name},
    
Thank you for adding in the mailing list.
    
Regards,
Farisy`;

console.log(another);
// while using template literals we can add placeholders
